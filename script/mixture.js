var fixed_menu_hidden = true;
function set_scroll(){
    var scrollTop = $(window).scrollTop();
    if(scrollTop>250){
        if(fixed_menu_hidden){
            $('#navscroll').fadeIn();
            fixed_menu_hidden = false;
        }
    }else{
        $('#navscroll').fadeOut();
        fixed_menu_hidden = true;
    }
}
window.onscroll = set_scroll;
