$(function() {
  function filterPath(string) {
  return string
    .replace(/^\//,'')
    .replace(/(index|default).[a-zA-Z]{3,4}$/,'')
    .replace(/\/$/,'');
  }
  var locationPath = filterPath(location.pathname);
  var scrollElem = scrollableElement('html', 'body');

  $('.anchor').each(function() {
    var thisPath = filterPath(this.pathname) || locationPath;
    if (locationPath == thisPath
    && (location.hostname == this.hostname || !this.hostname)
    && this.hash.replace(/#/,'') ) {
      var $target = $(this.hash), target = this.hash;
      if (target) {
        $(this).click(function(event) {
          var targetOffset = $target.offset().top;
          event.preventDefault();
          $(scrollElem).animate({scrollTop: targetOffset-48}, 1200);
        });
      }
    }
  });

  var $showMore = $('div.showmore > a');
  var $showTitle = $('div.content > a.content-title');
  $('div.hidden').hide(); 

  $showTitle.click(function() {
    var $name = jQuery(this).attr("id");
    var $hide = $('div#' + $name + ' div.hidden');
    var $target = $('div#' + $name);

    if ($hide.is(":visible")) {
      $(scrollElem).animate({scrollTop: $target.offset().top - 48}, 1200);
      $hide.hide('slow');
      $('div#' + $name).fadeTo(400, 0.5);
    } else {
      $(scrollElem).animate({scrollTop: $target.offset().top - 48}, 1200);
      $('div#' + $name).fadeTo(400,1);
      $hide.show('slow');
    }
    return false;
  });

  $showMore.click(function() {
    var $name = jQuery(this).attr("id");
    var $hide = $('div#' + $name + ' div.hidden');
    var $target = $('div#' + $name);

    if ($hide.is(":visible")) {
      $(scrollElem).animate({scrollTop: $target.offset().top - 48}, 1200);
      $hide.hide('slow');
      $('div#' + $name).fadeTo(400, 0.5);
    } else {
      $(scrollElem).animate({scrollTop: $target.offset().top - 48}, 1200);
      $('div#' + $name).fadeTo(400, 1);
      $hide.show('slow');
    }
    return false;
  });
  // use the first element that is "scrollable"
  function scrollableElement(els) {
    for (var i = 0, argLength = arguments.length; i <argLength; i++) {
      var el = arguments[i],
          $scrollElement = $(el);
      if ($scrollElement.scrollTop()> 0) {
        return el;
      } else {
        $scrollElement.scrollTop(1);
        var isScrollable = $scrollElement.scrollTop()> 0;
        $scrollElement.scrollTop(0);
        if (isScrollable) {
          return el;
        }
      }
    }
    return [];
  }
});
